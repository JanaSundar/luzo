import type { PreRequestRule, TestRule } from "@/types";

/**
 * Parses raw pre-request scripts back into PreRequestRule objects.
 */
export function parsePreRequestScript(script: string | undefined): PreRequestRule[] {
  if (!script) return [];

  const rules: PreRequestRule[] = [];
  for (const line of script.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    // Optimized regex for quoted strings: (['"])((?:[^\1\\]|\\.)*)\1
    const setEnvMatch = trimmed.match(
      /(?:pm|lz)\.env\.set\s*\(\s*(['"])((?:(?!\1)[^\\]|\\.)*)\1\s*,\s*(['"])((?:(?!\3)[^\\]|\\.)*)\3\s*\);?/,
    );
    if (setEnvMatch) {
      const rule = createKeyValueRule("set_env_var", setEnvMatch[2], setEnvMatch[4]);
      if (rule) rules.push(rule);
      continue;
    }

    const unsetEnvMatch = trimmed.match(
      /(?:pm|lz)\.env\.unset\s*\(\s*(['"])((?:(?!\1)[^\\]|\\.)*)\1\s*\);?/,
    );
    if (unsetEnvMatch) {
      const key = unsetEnvMatch[2];
      if (!key) continue;
      rules.push({ id: crypto.randomUUID(), type: "clear_env_var", key });
      continue;
    }

    const setHeaderMatch = trimmed.match(
      /(?:pm|lz)\.request\.headers\.upsert\s*\(\s*(['"])((?:(?!\1)[^\\]|\\.)*)\1\s*,\s*(['"])((?:(?!\3)[^\\]|\\.)*)\3\s*\);?/,
    );
    if (setHeaderMatch) {
      const rule = createKeyValueRule("set_header", setHeaderMatch[2], setHeaderMatch[4]);
      if (rule) rules.push(rule);
      continue;
    }

    const removeHeaderMatch = trimmed.match(
      /(?:pm|lz)\.request\.headers\.remove\s*\(\s*(['"])((?:(?!\1)[^\\]|\\.)*)\1\s*\);?/,
    );
    if (removeHeaderMatch) {
      const key = removeHeaderMatch[2];
      if (!key) continue;
      rules.push({ id: crypto.randomUUID(), type: "delete_header", key });
    }
  }

  return rules;
}

function createKeyValueRule(
  type: Extract<PreRequestRule["type"], "set_env_var" | "set_header">,
  key: string | undefined,
  value: string | undefined,
): PreRequestRule | null {
  if (!key || value === undefined) return null;
  return { id: crypto.randomUUID(), type, key, value };
}

/**
 * Parses raw test scripts back into TestRule objects.
 * This is best-effort and focuses on patterns generated by rule-compiler.ts.
 */
export function parseTestScript(script: string | undefined): TestRule[] {
  if (!script) return [];

  const rules: TestRule[] = [];

  // Regex to find lz.test blocks
  const testBlockRegex =
    /(?:pm|lz)\.test\s*\(\s*(['"])(.*?)\1\s*,\s*function\s*\(\s*\)\s*\{([\s\S]*?)\}\s*\);?/g;
  let match: RegExpExecArray | null = testBlockRegex.exec(script);

  while (match !== null) {
    const rawTestBody = match[3];
    if (!rawTestBody) {
      match = testBlockRegex.exec(script);
      continue;
    }
    const testBody = rawTestBody.trim();

    // 1. Status Code
    // lz.expect(lz.response.status).to.equal(Number("200"));
    const statusMatch = testBody.match(
      /(?:pm|lz)\.expect\s*\(\s*(?:pm|lz)\.response\.status\s*\)\.to\.(equal|not\.equal)\s*\(\s*Number\s*\(\s*(['"])(.*?)\2\s*\)\s*\);?/,
    );
    if (statusMatch) {
      const comparator = statusMatch[1];
      const value = statusMatch[3];
      if (!comparator || value === undefined) {
        match = testBlockRegex.exec(script);
        continue;
      }
      rules.push({
        id: crypto.randomUUID(),
        target: "status_code",
        operator: comparator === "equal" ? "equals" : "not_equals",
        value,
      });
    }

    // 2. Response Time
    // lz.expect(lz.response.time).to.be.below(Number("500"));
    const timeMatch = testBody.match(
      /(?:pm|lz)\.expect\s*\(\s*(?:pm|lz)\.response\.time\s*\)\.to\.be\.(above|below)\s*\(\s*Number\s*\(\s*(['"])(.*?)\2\s*\)\s*\);?/,
    );
    if (timeMatch) {
      const comparator = timeMatch[1];
      const value = timeMatch[3];
      if (!comparator || value === undefined) {
        match = testBlockRegex.exec(script);
        continue;
      }
      rules.push({
        id: crypto.randomUUID(),
        target: "response_time",
        operator: comparator === "above" ? "greater_than" : "less_than",
        value,
      });
    }

    // 3. Headers
    // Header existence: lz.expect(lz.response.headers.has("Content-Type")).to.be.true;
    const headerExistsMatch = testBody.match(
      /(?:pm|lz)\.expect\s*\(\s*(?:pm|lz)\.response\.headers\.has\s*\(\s*(['"])(.*?)\1\s*\)\s*\)\.to\.be\.(true|false);?/,
    );
    if (headerExistsMatch) {
      const property = headerExistsMatch[2];
      const existsFlag = headerExistsMatch[3];
      if (!property || !existsFlag) {
        match = testBlockRegex.exec(script);
        continue;
      }
      rules.push({
        id: crypto.randomUUID(),
        target: "header",
        property,
        operator: existsFlag === "true" ? "exists" : "not_exists",
      });
    }

    // Header value: lz.expect(lz.response.headers.get("Content-Type")).to.equal("application/json");
    const headerValueMatch = testBody.match(
      /(?:pm|lz)\.expect\s*\(\s*(?:pm|lz)\.response\.headers\.get\s*\(\s*(['"])(.*?)\1\s*\)\s*\)\.to\.(equal|include)\s*\(\s*(['"])(.*?)\4\s*\);?/,
    );
    if (headerValueMatch) {
      const property = headerValueMatch[2];
      const comparator = headerValueMatch[3];
      const value = headerValueMatch[5];
      if (!property || !comparator || value === undefined) {
        match = testBlockRegex.exec(script);
        continue;
      }
      rules.push({
        id: crypto.randomUUID(),
        target: "header",
        property,
        operator: comparator === "equal" ? "equals" : "contains",
        value,
      });
    }

    // 4. Body Contains
    // lz.expect(lz.response.text()).to.include("search");
    const bodyMatch = testBody.match(
      /(?:pm|lz)\.expect\s*\(\s*(?:pm|lz)\.response\.text\(\)\s*\)\.to\.(equal|include|not\.include)\s*\(\s*(['"])(.*?)\2\s*\);?/,
    );
    if (bodyMatch) {
      const comparator = bodyMatch[1];
      const value = bodyMatch[3];
      const opMap: Record<string, TestRule["operator"]> = {
        equal: "equals",
        include: "contains",
        "not.include": "not_contains",
      };
      const operator = comparator ? opMap[comparator] : undefined;
      if (!operator || value === undefined) {
        match = testBlockRegex.exec(script);
        continue;
      }
      rules.push({
        id: crypto.randomUUID(),
        target: "body_contains",
        operator,
        value,
      });
    }

    // 5. JSON Property
    // lz.expect(String(_.get(__jsonData, "data.id"))).to.equal(String("123"));
    const jsonPropMatch = testBody.match(
      /(?:pm|lz)\.expect\s*\(\s*(String|Number)\s*\(\s*_\.get\s*\(\s*__jsonData\s*,\s*(['"])(.*?)\2\s*\)\s*\)\s*\)\.to(\.not)?\.(equal|include|be\.above|be\.below)\s*\(\s*(String|Number)\s*\(\s*(['"])(.*?)\7\s*\)\s*\)\s*\);?/,
    );
    if (jsonPropMatch) {
      const property = jsonPropMatch[3];
      const isNot = !!jsonPropMatch[4];
      const op = jsonPropMatch[5];
      const value = jsonPropMatch[8];
      if (!property || !op || value === undefined) {
        match = testBlockRegex.exec(script);
        continue;
      }

      let operator: TestRule["operator"] = "equals";
      if (op === "equal") operator = isNot ? "not_equals" : "equals";
      else if (op === "include") operator = isNot ? "not_contains" : "contains";
      else if (op === "be.above") operator = "greater_than";
      else if (op === "be.below") operator = "less_than";

      rules.push({
        id: crypto.randomUUID(),
        target: "json_property",
        property,
        operator,
        value,
      });
    }

    match = testBlockRegex.exec(script);
  }

  return rules;
}
